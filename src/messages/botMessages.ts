export function contactsMessage(): string {
  const lines = ["Контакты для связи:"];

  if (config.ownerTelegramUsername) {
    lines.push(`Telegram: @${config.ownerTelegramUsername}`);
  }

  if (config.publicChannelUrl) {
    lines.push(`Канал: ${config.publicChannelUrl}`);
  }

  if (config.publicSiteUrl) {
    lines.push(`Сайт: ${config.publicSiteUrl}`);
  }

  lines.push("", "Чтобы оставить заявку, нажмите /start.");

  return lines.join("\n");
}
